#Вставка "ДО" .insert(x, y)
#вставляет элемент y перед позицией x в списке
a = [5, 3, 2, 1]
a.insert(1, 4)
print(a)
#Вывод:
#[5, 4, 3, 2, 1]

#Сортировка значений .sort()
a = [1, 5, 3, 2, 4]
a.sort()
print(a)
#Вывод:
#[1, 2, 3, 4, 5]

#Сортировка в порядке убывания .sort(reverse=True)
a = [1, 5, 3, 2, 4]
a.sort(reverse=True)
print(a)
#Вывод:
#[5, 4, 3, 2, 1]

#Удалить все элементы .clear
