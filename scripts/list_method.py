print(a)
a.clear()
print(a)
#Вывод:
#[1, 2, 3, 4, 5]
#[]

#Удалить элемент .pop(x)
#x - индекс удаляемого элемента
a = [5, 4, 3, 2, 1]
a.pop(1)
print(a)
#Вывод:
#[5, 3, 2, 1]

#Удалить по значению .remove(x)
#Удаляет из списка элемент со значением x
a = [5, 4, 3, 2, 1]
if (4 in a): #4 есть в списке a?
    a.remove(4)
print(a)
#Вывод:
#[5, 3, 2, 1]

#Копия списка .copy()
a = [1, 2, 3, 4, 5]
b = a.copy()
print(b)
#Вывод:
#[1, 2, 3, 4, 5]

#подсчёт количества .count(x)
#сколько встречается элемент со значением x
a = [1, 2, 2, 1, 1]
b = a.count(1)
print(b)
#Вывод:
#3

#Расширить список .extend(x)
#x - итерабельная переменная
a = [0, 1, 2]
s = "ABC"
a.extend(s)
print(a)
#Вывод:
#[0, 1, 2, 'A', 'B', 'C']

#Получить индекс .index(x)
